#include <unistd.h>

void	stampa(int n1, int n2)
{
	char	a;

	a = (n1 / 10) + 48;
	write(1, &a, 1);
	a = (n1 % 10) + 48;
	write(1, &a, 1);
	write(1, " ", 1);
	a = (n2 / 10) + 48;
	write(1, &a, 1);
	a = (n2 % 10) + 48;
	write(1, &a, 1);
	if (n1 != 98)
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int	n1;
	int	n2;

	n1 = 0;
	n2 = 1;
	while (n1 <= 98)
	{
		while (n2 <= 99)
		{
			if (n1 < n2)
			{
				stampa(n1, n2);
			}
			n2++;
		}
		n2 = 0;
		n1++;
	}
}
